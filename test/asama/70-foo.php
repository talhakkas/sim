<h1>Çiçek Serası Çalışanı merdivenden düştü</h1>

Hastanın uygun şartlarda transportu acil olarak sağlandı. Sonrasında havayolu,
solunum ve dolaşım değerlendirildi. Olayın öyküsü soruldu.<br>

<img src=etc/olgu.jpg>

<br>

Resimde görüldüğü şekilde sera içinde ilaçlama yaptığını, sonrasında bulantı
hissinin olduğunu, ancak işlerini bugün tamamlaması gerektiğini bu nedenle
seranın içinde tamirat işi için çıktığı merdiven üzerinde iken fenalaşıp 5
basamaklı merdivenden düştüğünü, seranın zemininde bulunan beton çıktıya
çarptığını, boynunu ve belini incittiğini, halen bulantısı ve terlemeleri
olduğunu belirtti. <hr>

Halen açık alanda bir set zemin üzerinde yatan hastanın gömleğinin yaka
bölgesinde boyundaki künt travmatik yaralanmadan kaynaklanan kan bulaşı
görülmekte, aktif kanama  izlenmemektedir. Düşündüğünüz ön tanılar
doğrultusunda, hastanın tedavisinde acilen yapılması gereken hangi işlem
hastanın yaşamının kurtulması açısından önemlidir ? <br>

Hastanın gelen ambulans ile tam teşekkülü devlet hastanesi acil servisine sevki
gerçekleşmiştir.  Ambulans gelene kadar bol ılık su ile vücudu yıkanmış ve
üzerine temiz bir çarşaf örtülmüştür. Tarım ilacı ile kontaminasyon şüphesi
olan elbiseleri imha edilmek üzere tehlikeli tıbbi atık çöpüne atılmıştır.

